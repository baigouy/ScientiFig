    public ListOfShortcuts(LinkedHashMap<String, String> shortcuts) {
        initComponents();
        int max_size = 0;
        for (String shortcut : shortcuts.keySet()) {
            max_size = Math.max(max_size, shortcut.length());
        }
        ((GridLayout) jPanel2.getLayout()).setRows(shortcuts.size());
        for (String shortcut : shortcuts.keySet()) {
            jPanel2.add(new JLabel(shortcut + " : "));
            jPanel2.add(new JLabel(shortcuts.get(shortcut)));
        }
        jPanel2.revalidate();
    }

